int	ft_str_is_uppercase(char *str)
{
	int i;
	int x;
	int y;

	x = 0;
	y = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			i++;
		}
		else
		{
			return (x);
		}
	}
	return (y);
}
